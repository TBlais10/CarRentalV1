package com.company;

public class Car {

    private String model;
    private String name;
    private String color;
    private int price;
    private boolean isRented;


    public Car(String model, String name, String color, int price, boolean isRented) {
        this.model = model;
        this.name = name;
        this.color = color;
        this.price = price;
        this.isRented = isRented;
    }

}
